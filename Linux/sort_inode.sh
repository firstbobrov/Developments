#!/bin/bash
# Скрипт для сортировки файлов по их индексным дескрипторам (inode)

# Проверяем, передан ли каталог в качестве аргумента
if [ -z "$1" ]; then
  echo "Использование: $0 <путь_к_каталогу>"
  exit 1
fi

# Переходим в указанный каталог
cd "$1" || { echo "Ошибка: не могу войти в каталог $1"; exit 1; }

# Выводим файлы с индексными дескрипторами, сортируем и выводим только имена файлов
ls -i | sort -n | awk '{print $2}'
